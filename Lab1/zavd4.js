// zavd4.js — розрахунок вартості товару та формування чеку

const товар = 'Ноутбук';
const ціна_базова = 20000; // за одиницю
const кількість = 2;
const націнка = 0.20; // 20%
const ПДВ_ставка = 0.20; // 20%

const ціна_з_націнкою = +(ціна_базова * (1 + націнка)).toFixed(2);
const сума_без_пдв = +(ціна_з_націнкою * кількість).toFixed(2);
const пдв = +(сума_без_пдв * ПДВ_ставка).toFixed(2);
const загальна_сума = +(сума_без_пдв + пдв).toFixed(2);

console.log('=== ТОВАРНИЙ ЧЕК ===');
console.log('Назва товару:', товар);
console.log('Кількість:', кількість);
console.log('Ціна за одиницю (базова):', ціна_базова);
console.log('Ціна за одиницю (з націнкою):', ціна_з_націнкою);
console.log('Сума без ПДВ:', сума_без_пдв);
console.log('ПДВ (20%):', пдв);
console.log('Загальна сума до сплати:', загальна_сума);

